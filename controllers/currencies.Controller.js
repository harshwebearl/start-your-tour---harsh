  async add_currencies(req, res) {
    try {
      const tokenData = req.userData;
      if (tokenData === "") {
        return res.status(401).json({
          message: "Auth fail"
        });
      }
      const userData = await userSchema.find({ _id: tokenData.id });
      if (userData[0].role !== "agency") {
        throw new Forbidden("you are not agency");
      }

      const data = {
        agency_id: tokenData.id,
        name: req.body.name,
        // icon: req.body.icon,
        icon: req.files.icon[0].filename,
        // icon: generateFilePathForDB(req.file),
        code: req.body.code,
        purchase_price: req.body.purchase_price,
        selling_price: req.body.selling_price
      };

      const add_currencies = new currencies_schema(data);
      const Add_currencies = await add_currencies.save();

      // Add_currencies.icon = generateDownloadLink(Add_currencies.icon);

      return this.sendJSONResponse(
        res,
        "add currencies ",
        {
          length: 1
        },
        Add_currencies
      );
    } catch (error) {
      if (error instanceof NotFound) {
        console.log(error); // throw error;
      }
      return this.sendErrorResponse(req, res, error);
    }
  }

